"""Scrape pytest output and output LAVA signal strings."""

import argparse
import os
import re
import sys


def setup_parser():
    """Create command line parser.

    :return: parser object.

    """
    parser = argparse.ArgumentParser(description="Send Pytest Results To LAVA")
    parser.add_argument(
        "input",
        type=str,
        default="./results.txt",
        help="Results file generated by pytest",
    )
    return parser


def CreateLavaOutputText(line, result):
    """Process a Line of results data and turn it into LAVA speak.

    :return: string containing the LAVA formatted result

    """
    lava_signal = "<LAVA_SIGNAL_TESTCASE TEST_CASE_ID="
    result_str = "RESULT="
    terminator = ">"

    name = line.split("::", 1)[1].split(" ")[0]

    return "{}{} {}{}{}".format(
        lava_signal, name, result_str, result, terminator
    )


def main():
    """Perform the main execution."""
    # Parse command line
    options = setup_parser().parse_args()

    # results store
    results = []

    # setup regular expressions for PASSED and FAILED
    pass_re = re.compile(".*::.*::.*PASSED")
    fail_re = re.compile(".*::.*::.*(FAILED|ERROR)")

    # Look for the results file and process it if it exists
    if os.path.isfile(options.input):
        with open(options.input) as file:
            for line in file:
                if pass_re.search(line):
                    results.append(CreateLavaOutputText(line, "pass"))
                elif fail_re.search(line):
                    results.append(CreateLavaOutputText(line, "fail"))
    else:
        print("Input File not found")

    for result in results:
        print(result)


if __name__ == "__main__":
    sys.exit(main())
